// This is the initialization of the PWM
void pwm_init()
{
  //TCNT0 = 0;
  TCCR0A = (0xA3);
  TCCR0B = (0x01);
  TCCR1A = (0x81);
  TCCR1B = (0x01); // (0x09)
  // TCNT0 = 0;
  
}

//============================================================================


// pull in adc quarter and update pwms
void update_pwm(uint8_t target_led)
{
    
    //RED
    if (0 == target_led)
    {
      update_dc(255, 0, 0);      
    }
    
    // YELLOW
    else if (1 == target_led)
    {     
      update_dc(255, 255, 0);       
    }
    
    // GREEN
    else if (2 == target_led)
    {
      update_dc(0, 255, 0);
    }
    
    // CYAN
    else if (3 == target_led)
    {
      update_dc(0, 255, 255);
    }
    
    else if (4 == target_led)
    {
      update_dc(0, 0, 0);
    }    

}

