// takes in desired color value, returns if complete or not.
uint8_t update_dc(uint8_t red, uint8_t green, uint8_t blue)
{ 
  
  // RED
  if (red_pwm > red)
  {
    OCR0B = red_pwm--;
  }
  else if(red_pwm < red)
  {
    OCR0B = red_pwm++;
  }
  
  // GREEN
  else if (green_pwm > green)
  {    
    OCR0A = green_pwm--;
  }
  else if(green_pwm < green)
  {
    OCR0A = green_pwm++;
  }
  
  // BLUE
  else if (blue_pwm > blue)
  {
    OCR1AH = 0;
    OCR1AL = blue_pwm--;
  }
  else if(blue_pwm < blue)
  {
    OCR1AH = 0;
    OCR1AL = blue_pwm++;
  }
  
  return 0;

}

