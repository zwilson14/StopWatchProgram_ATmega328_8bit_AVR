//==============================================================================
// Initialize the switch
void switch_init()
{
  // Set pin for switch to input
  DDRB &= ~(1 << 4);
}

//==============================================================================

// returns the switch value as a boolean
bool switch_get()
{
  bool retval = false;
  if ((PINB & (1 << 4)) != 0) // active high
  {
    retval = true;
  }
  return retval;
}

