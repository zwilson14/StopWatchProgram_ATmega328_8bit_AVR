void led_init()
{
  // R0 = PD5 - PWM
  // G0 = PD6 - PWM
  // B0 = PB1 - PWM
  // R1 = PD4
  // G1 = PB2 - PWM
  // B1 = PB0
  //
  // PORT B
  // 7  6  5  4  3  2  1  0
  // -  -  -  -  - G1 B0 B1
  //
  // PORT D
  // 7  6  5  4  3  2  1  0
  // - G0 R0 R1  -  -  -  -

  PORTB &= ~0x07;
  DDRB |= 0x07;
  // same as:
  // 0x00000111

  PORTD &= ~0x70;
  DDRD |= 0x70;
}

